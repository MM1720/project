package $6_Bit_Manipulation;

public class $1_bitwise_operator {
    public static void main(String[] args) {

        System.out.println(5 & 6);
        System.out.println(5 | 6);
        System.out.println(5 ^ 6);
        System.out.println(~ 5);
        System.out.println(6 << 2);
        System.out.println(6 >> 1);
    }
}
